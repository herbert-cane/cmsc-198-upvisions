using UnityEngine;

[System.Serializable]  // Add this line to make it serializable
public class Quest
{
    public string questTitle;       // The title of the quest
    public string questDescription; // The description of the quest
    public bool isCompleted;        // If the quest is completed

    public Quest(string title, string description)
    {
        questTitle = title;
        questDescription = description;
        isCompleted = false;
    }
}
